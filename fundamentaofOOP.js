//? ✅ Syntax (language constructs)
//* These are ways JavaScript allows you to create or structure objects, but they themselves are not OOP concepts.
//* - Object Literal
//* - Constructor Function
//* - Classes (ES6)

//? ✅ OOP Concepts (theory/mechanisms)
//* These are the actual principles or mechanisms of object-oriented programming.
//* Prototypes — JS’s inheritance system
//* Instance — object created from a class/constructor

//? Why this separation?
//* Object literal is just a syntax to create objects.
//* Constructor functions and classes are syntax for defining templates to create objects.
//* Prototypes define inheritance → this is actual OOP behavior.
//* Instances represent objects created from templates → fundamental OOP idea.